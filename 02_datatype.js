/* DataTypes in JavaScript

1. String:
   - A sequence of characters.
   - Example: "Abhishek Sharma", "Hello"

2. Number:
   - Numeric values, may contain decimal values.
   - Example: 0, 3, 4, 3.5, 10

3. Boolean:
   - Represents true or false values.
   - Example: true, false

4. BigInt:
   - For representing very large numbers.
   - Example: 1234567890123456789012345678901234567890n

5. Undefined:
   - Indicates a variable that has been declared but not assigned a value.
   - Example: let x; (x is undefined)

6. null:
   - Represents an empty or non-existent value.
   - Note: null is different from 0.
   - Example: let y = null;

7. Object:
   - A collection of properties (key-value pairs).
   - Can include arrays, dictionaries, and more.
   - Example: { name: "Abhishek", age: 25 }

8. Symbol:
   - A unique and immutable primitive value.
   - Example: let sym = Symbol("description");

*/

